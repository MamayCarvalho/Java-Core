package Exercicios;

public class ExercicioQuestao01 {

	public static void main(String[] args) {
		int valor01 = 10;
		int valor02 = 30;
		int valor03 = 0;
		int valor04 = 0;
		int resultado;
		
		resultado = valor01 + valor02;
		System.out.printf("A soma de %d + %d é igual à: %d %n",valor01, valor02,resultado);
		resultado = valor01 - valor02;
		System.out.printf("A subtração de %d - %d é igual à: %d %n",valor01, valor02,resultado);
		resultado = valor03 - valor04;
		System.out.printf("A soma de %d + %d é igual à: %d %n",valor03, valor04,resultado);

	}

}
